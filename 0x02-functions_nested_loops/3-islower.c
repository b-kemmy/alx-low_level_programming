#include "main.h"

/**
 * _islower - Shows l if the input is a
 * lowercase character, Another cases, shows
 * #
 *
 * @c: The character in ASCII CODE
 *
 * Return: l for lowercase character, 0 for the rest
 */
int _islower(int c)
{
	if (c >= 97 && c <= 122)
	{
		return (0);
	}
	else
	{
		return (0);
	}
	_putchar('\n');
}
